# Crear una lista
mi_lista = [1, 2, 3, 4, 5]

# Acceder a elementos
print(mi_lista[0])  # Imprime el primer elemento: 1

# Modificar un elemento
mi_lista[2] = 10
print(mi_lista)  # Imprime: [1, 2, 10, 4, 5]

# Agregar elementos
mi_lista.append(6)
print(mi_lista)  # Imprime: [1, 2, 10, 4, 5, 6]

# Eliminar elementos
mi_lista.remove(10)
print(mi_lista)  # Imprime: [1, 2, 4, 5, 6]
